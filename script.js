// --- Insert search bar ---
function insertSearchBar() {
    const searchArea = document.getElementById("searchArea");

    const wrapper = document.createElement("div");
    wrapper.className = "search-wrapper";

    const input = document.createElement("input");
    input.className = "search-input";
    input.id = "searchInput";
    input.placeholder = "Search token or contract...";

    input.addEventListener("keyup", filterTokens);

    wrapper.appendChild(input);
    searchArea.appendChild(wrapper);
}


// --- Render tokens ---
function renderTokens(list = tokens) {
    const mainArea = document.getElementById("mainCardArea");
    const listArea = document.getElementById("tokenList");
    mainArea.innerHTML = "";
    listArea.innerHTML = "";

    list.forEach(t => {

        listArea.innerHTML += `
            <div class="token-card">
                <div class="logo-text">
                    <img src="${t.logo}" class="token-logo">
                    <div class="token-name">${t.name}</div>
                </div>
                <div class="contract">${t.address}</div>

                <div class="button-row">
                    <button class="btn" onclick="copyAddress('${t.address}')">Copy</button>
                    <button class="btn" onclick="window.open('https://atcscan.io/address/${t.address}', '_blank')">ATCScan</button>
                </div>
            </div>
        `;
    });
}

// --- Filter tokens by search input ---
function filterTokens() {
    const v = document.getElementById("searchInput").value.toLowerCase();
    const value = document.getElementById("sortSelect").value;

    let filtered = tokens.filter(t =>
        t.name.toLowerCase().includes(v) ||
        (t.address && t.address.toLowerCase().includes(v))
    );

    // Apply category filter if selected
    if (value !== "all") {
        filtered = filtered.filter(t => t.category === value);
    }

    // Sort alphabetically A-Z
    filtered.sort((a, b) => a.name.localeCompare(b.name));

    renderTokens(filtered);
}

// --- Sort tokens by category or A-Z ---
function sortTokens() {
    const value = document.getElementById("sortSelect").value;

    let list = [...tokens];

    // Apply category filter if not "all"
    if (value === "stable" || value === "utility" || value === "meme") {
        list = list.filter(t => t.category === value);
    }

    // Always sort alphabetically A-Z
    list.sort((a, b) => a.name.localeCompare(b.name));

    // Apply search filter if anything is typed
    const searchInput = document.getElementById("searchInput");
    if (searchInput && searchInput.value) {
        const v = searchInput.value.toLowerCase();
        list = list.filter(t =>
            t.name.toLowerCase().includes(v) ||
            (t.address && t.address.toLowerCase().includes(v))
        );
    }

    renderTokens(list);
}

// --- Copy address ---
function copyAddress(addr) {
    navigator.clipboard.writeText(addr);
    showCopied();
}

function showCopied() {
    let box = document.querySelector(".copy-alert");

    if (!box) {
        box = document.createElement("div");
        box.className = "copy-alert";
        box.textContent = "Copied!";
        document.body.appendChild(box);
    }

    box.classList.add("show");
    setTimeout(() => box.classList.remove("show"), 1500);
}

